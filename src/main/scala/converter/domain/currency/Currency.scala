object Currency:
  import converter.domain.rate.RateExceptions

  def fromString(str: String): Either[RateExceptions.CurrencyNotSupported, Currency] =
    Currency.values.find(_.code == str) match {
      case Some(currency) => Right(currency)
      case None => Left(RateExceptions.CurrencyNotSupported(str))
    }
